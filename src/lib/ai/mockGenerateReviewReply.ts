import type { Review } from "@/lib/types";

// 低評価(3以下)向け: 感謝 + 謝罪 + 改善姿勢
const apologyTemplates = [
  (r: Review) =>
    `この度はご来店いただき、また貴重な口コミを投稿していただき誠にありがとうございます。${r.reviewerName.split(" ")[0]}様にご不便をおかけしましたこと、心よりお詫び申し上げます。頂いたご意見を真摯に受け止め、サービスの改善に努めてまいります。今後ともよろしくお願いいたします。`,
  (r: Review) =>
    `口コミをお寄せいただきありがとうございます。この度はご期待に沿えず、誠に申し訳ございませんでした。「${r.category}」に関する貴重なご指摘として、スタッフ全員で共有し、改善に取り組んでまいります。またの機会がございましたら、ぜひお立ち寄りいただけますと幸いです。`,
  () =>
    `この度は貴重なご意見をお寄せいただき、誠にありがとうございます。ご不快な思いをさせてしまいましたこと、深くお詫び申し上げます。頂戴したご指摘は今後の運営改善に必ず活かしてまいります。またご来店いただける機会がございましたら、より良いサービスでお迎えできるよう努めてまいります。`,
];

// 高評価向け: 感謝 + 内容への言及
const thanksTemplates = [
  (r: Review) =>
    `この度はご来店いただき、また温かい口コミを投稿していただき誠にありがとうございます。「${r.category}」についてお褒めの言葉を頂戴し、スタッフ一同大変嬉しく思っております。今後もご満足いただけるサービスを提供できるよう努めてまいります。またのご来店を心よりお待ちしております。`,
  () =>
    `嬉しい口コミを投稿していただき、誠にありがとうございます。お褒めの言葉はスタッフの大きな励みになります。今後もお客様に安心してご利用いただける店舗づくりを大切にしてまいります。またお会いできる日を楽しみにしております。`,
  () =>
    `この度はご来店いただき、また口コミへの投稿もいただき、誠にありがとうございます。ご満足いただけたご様子を伺い、大変嬉しく思います。今後もより良いサービスをお届けできるよう、日々努めてまいります。またのご来店を心よりお待ちしております。`,
];

export async function mockGenerateReviewReply(
  review: Review,
  variant = 0,
): Promise<string> {
  await new Promise((resolve) => setTimeout(resolve, 500));
  const templates = review.rating <= 3 ? apologyTemplates : thanksTemplates;
  return templates[variant % templates.length](review);
}
